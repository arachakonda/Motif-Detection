/**
  * @brief This function handles TIM7 global interrupt, DAC2 and DAC4 channel underrun error interrupts.
  */
void TIM7_DAC_IRQHandler(void)
{
  /* USER CODE BEGIN TIM7_DAC_IRQn 0 */
	
	msTicks_T_7++;
	M1.pre_current_sense = M1.current_current_sense;
	M2.pre_current_sense = M2.current_current_sense;
	M3.pre_current_sense = M3.current_current_sense;
	M4.pre_current_sense = M4.current_current_sense;
	
	KalArm_EMG_CLOSE.pre_pattern = KalArm_EMG_CLOSE.pattern;
	KalArm_EMG_OPEN.pre_pattern = KalArm_EMG_OPEN.pattern;

  /* USER CODE END TIM7_DAC_IRQn 0 */
  HAL_TIM_IRQHandler(&htim7);
  /* USER CODE BEGIN TIM7_DAC_IRQn 1 */
	
	if(KalArm_Calibration_Flag == 1){
	

			if(HAL_GPIO_ReadPin(REQ_CLK_GPIO_Port, REQ_CLK_Pin)== GPIO_PIN_SET){
						HAL_GPIO_WritePin(REQ_CLK_GPIO_Port, REQ_CLK_Pin, GPIO_PIN_RESET); // send data only when not receiving data from master
			}
	
	}
	
	if(KalArm_EMG_Flag == 1){
		
		if(KalArm_MODE.mode_number == 0 || KalArm_MODE.mode_number == 1 || KalArm_MODE.mode_number == 2 || KalArm_MODE.mode_number == 3){
			if(KalArm_EMG_CLOSE.trigger == TRIGGER_NOT_SET){
				
				
				if(KalArm_MODE.mode_number == 2){
					txData_SPI[0]=0x90;
					txData_SPI[1]=0x00;
					txData_SPI[2]=0x00;
					txData_SPI[2]=KalArm_MODE.mode_3_grip_id;
					txData_SPI[3]=0x0B;
					txData_SPI[3]|= 1<<4; // command 1
					txData_SPI[3]&= ~(1<<7); // trigger not set in mode 3
				}
				else{
					txData_SPI[0]=0x90;
					txData_SPI[1]=0x00;
					txData_SPI[2]=0x00;
					txData_SPI[3]=0x0B;
					txData_SPI[3]|= 1<<4;
					txData_SPI[3]&= ~(1<<7);			
				
				}
			
			}
			else if(KalArm_EMG_CLOSE.trigger == TRIGGER_SET){
				txData_SPI[0]=0x90;
				txData_SPI[1]=0x00;
				txData_SPI[2]=0x00;
				txData_SPI[3]=0x0B;
				txData_SPI[3]|= 1<<4;
				txData_SPI[3]|= (1<<7);
			
			}
			else{
				

			
			}
		}
		
		if(KalArm_GEN_GRIP_STAT_RES.grip_execute_order == 1){
			// indicate general grip test result

		
		}
		

	}
	
	
	
	if(DIFFERENTIAL_LOW_PASS_SHIFT){
		
		M1.previous_index_counts = M1.current_index_counts;
		M2.previous_index_counts = M2.current_index_counts;
		M3.previous_index_counts = M3.current_index_counts;
		M4.previous_index_counts = M4.current_index_counts;
		
		M1.previous_diff = M1.current_diff;
		M2.previous_diff = M2.current_diff;
		M3.previous_diff = M3.current_diff;
		M4.previous_diff = M4.current_diff;
	}
	else if(DIFFERENTIAL_LOW_PASS_CAPTURE){
		
		M1.current_diff  =  (int)((M1.current_index_counts - M1.previous_index_counts)/DIFFERENTIAL_FACTOR);
		M2.current_diff  =  (int)((M2.current_index_counts - M2.previous_index_counts)/DIFFERENTIAL_FACTOR);
		M3.current_diff  =  (int)((M3.current_index_counts - M3.previous_index_counts)/DIFFERENTIAL_FACTOR);
		M4.current_diff  =  (int)((M4.current_index_counts - M4.previous_index_counts)/DIFFERENTIAL_FACTOR);
		
		M1.curve = (int)((M1.current_diff - M1.previous_diff));
		M2.curve = (int)((M2.current_diff - M2.previous_diff));
		M3.curve = (int)((M3.current_diff - M3.previous_diff));
		M4.curve = (int)((M4.current_diff - M4.previous_diff));
		
	}


  /* USER CODE END TIM7_DAC_IRQn 1 */
}

